package Day5;

public class BubbleSort {

	public static void main(String[] args) {
		// ++i. --i
		//i++, i--
	for (int i=0; i<=4; i++)
	{
		System.out.println("HeLLO");
	}
	
	for (int i=0; i<=4; ++i)
	{
		System.out.println("Hi");
	}
	
	for (int i=4; i>=0; --i)
	{
		System.out.println("Welcome");
	}
	
	
	
	}

}
